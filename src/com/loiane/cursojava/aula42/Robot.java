package com.loiane.cursojava.aula42;

public final class Robot {
	
	private String model;
	private String code;
	private String YearFabrication;
	private String type;
	
	
	
	
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getYearFabrication() {
		return YearFabrication;
	}
	public void setYearFabrication(String yearFabrication) {
		YearFabrication = yearFabrication;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}	

}
